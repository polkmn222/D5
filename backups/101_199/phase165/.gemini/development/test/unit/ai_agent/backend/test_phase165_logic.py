import pytest
from unittest.mock import patch, MagicMock
from db.models import Lead
from ai_agent.backend.service import AiAgentService

@pytest.fixture
def mock_db():
    return MagicMock()

@pytest.mark.asyncio
async def test_lead_create_auto_open(mock_db):
    mock_lead = Lead(id="L1", last_name="Hong", status="New")
    
    with patch("ai_agent.backend.service.IntentPreClassifier.detect", return_value=None):
        with patch("ai_agent.backend.service.AiAgentService._call_multi_llm_ensemble", return_value={"intent": "CREATE", "object_type": "lead", "data": {"last_name": "Hong"}}):
            with patch("web.backend.app.services.lead_service.LeadService.create_lead", return_value=mock_lead):
                response = await AiAgentService.process_query(
                    db=mock_db,
                    user_query="create lead Hong",
                    conversation_id="conv-1"
                )
                
                assert response["intent"] == "CREATE"
                assert response.get("auto_open") is True
                assert response.get("record_id") == "L1"

