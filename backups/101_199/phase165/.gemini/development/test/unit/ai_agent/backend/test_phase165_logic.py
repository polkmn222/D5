@pytest.mark.asyncio
async def test_object_type_normalization(mock_db):
    # Testing that 'leads' is normalized to 'lead' in the response
    with patch("ai_agent.backend.service.IntentPreClassifier.detect", return_value={"intent": "QUERY", "object_type": "leads"}):
        with patch("ai_agent.backend.service.AiAgentService._execute_paginated_query", return_value={"results": [], "sql": "SELECT...", "pagination": {}}):
            response = await AiAgentService.process_query(
                db=mock_db,
                user_query="all leads",
                conversation_id="conv-1"
            )
            # The singularization logic in _execute_intent should have turned 'leads' into 'lead'
            assert response["object_type"] == "lead"
